public class MergeSortedArray_88 {
    public void merge(int[] nums1, int m, int[] nums2, int n) {
        for (int i = 0; i < n; i++) {
            System.out.println(nums2[n]);
            for (int j = 0; j < m; j++) {
                System.out.println(nums1[m]);
            }
        }
    }
}
